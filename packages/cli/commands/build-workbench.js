// `rig build-workbench [-o paper-rig-workbench.html]` — regenerate the workbench
// as a single self-contained HTML from the extracted sources, making the HTML a
// build artifact rather than the source of truth.
//
// The workbench must run from file:// (double-click), where `<script type=module
// src>` is blocked by CORS, so the split ESM is re-flattened into the one classic
// <script> it came from: schema + compiler pipeline (from the packages) + every
// creature resolved from its declarative model (injected as data) + the DOM/UI
// layer, concatenated in dependency order with import/export stripped and the
// injected render context folded back onto the UI's live `state`.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';
import { parseArgs } from '../lib/args.js';
import { loadModel, loadModelSource } from '@paper-rig/rigs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '../../..');
const read = (p) => readFileSync(join(ROOT, p), 'utf8');

// The workbench's model selector and `state` init key off `Object.keys(rigs)`, so
// the rig table must preserve the original authoring order, not alphabetical.
const RIG_ORDER = [
  'horse', 'humanoid', 'tinyBiped', 'stoutBiped', 'tailedBiped', 'rhino', 'elephant',
  'rabbit', 'leopard', 'wolf', 'greyhound', 'blob', 'quadruped', 'lizard', 'dragon',
  'walkingBird', 'flyingBird', 'bug', 'ant', 'radial', 'serpentine', 'mimic', 'tentacle',
  'scorpion', 'bat', 'crab', 'lobster', 'centaur', 'harpy', 'angel', 'spider',
];

// Resolve every declarative model to its rig and emit `const rigs = {...}` as data,
// replacing the old imperative builders. Models are proven byte-identical to the
// original rigs by test/resolver.test.mjs and the workbench parity gate.
function resolvedRigsSource() {
  const rigs = {};
  for (const name of RIG_ORDER) rigs[name] = loadModel(name);
  return 'const rigs = ' + JSON.stringify(rigs) + ';';
}

// The editor's source-patch preview needs the thin declarative model alongside
// the resolved rig. It is read-only browser data: source writes remain an
// explicit maintainer/agent action outside the generated workbench.
function modelSourcesSource() {
  const modelSources = {};
  for (const name of RIG_ORDER) modelSources[name] = loadModelSource(name);
  return 'const modelSources = ' + JSON.stringify(modelSources) + ';';
}

// schema/index.js -> plain script: drop the `export ` on each declaration.
function flattenSchema(src) {
  return src.replace(/^export\s+/gm, '');
}

// authoring-patch.js -> plain script: cloneData comes from the flattened schema;
// remove its ESM import and public export markers.
function flattenAuthoringPatch(src) {
  return src
    .replace(/^import\s.*$/gm, '')
    .replace(/^export\s+/gm, '');
}

// compiler/core.js -> plain script: drop the schema import, drop the injected
// `state` context (the UI's `state` serves in the flattened workbench), drop the
// public re-export, and fold `state.rig` back to the global rig table.
function flattenCompiler(src) {
  return src
    .replace(/^import\s.*$/gm, '')
    .replace(/^export const state = \{[\s\S]*?^\};$/m, '')
    .replace(/^export \{[\s\S]*?\};$/m, '')
    .replaceAll('state.rig', 'rigs[state.model]');
}

export function runBuildWorkbench(argv) {
  const { flags } = parseArgs(argv, { o: 'out' });

  const bundle = [
    '// GENERATED by `rig build-workbench` from packages/ + rigs/ + apps/workbench/.',
    '// Do not edit here — edit the sources and regenerate. See CLAUDE.md.',
    '',
    '// ---- @paper-rig/schema ----',
    flattenSchema(read('packages/schema/index.js')),
    '// ---- source-safe authoring patch generation ----',
    flattenAuthoringPatch(read('rigs/authoring-patch.js')),
    '// ---- @paper-rig/compiler (pure pipeline + validation) ----',
    flattenCompiler(read('packages/compiler/core.js')),
    '// ---- creatures (resolved from rigs/models/*.json, injected as data) ----',
    resolvedRigsSource(),
    '// ---- declarative model sources (read-only patch targets) ----',
    modelSourcesSource(),
    '// ---- workbench DOM/UI ----',
    read('apps/workbench/ui.js'),
  ].join('\n');

  const template = read('apps/workbench/template.html');
  if (!template.includes('/* PAPER_RIG_BUNDLE */')) {
    console.error('template.html is missing the /* PAPER_RIG_BUNDLE */ slot');
    return 1;
  }
  const html = template.replace('/* PAPER_RIG_BUNDLE */', () => bundle);

  const out = flags.out ? resolve(process.cwd(), flags.out) : join(ROOT, 'paper-rig-workbench.html');
  writeFileSync(out, html);
  console.log(`regenerated ${out} (${(html.length / 1024).toFixed(0)} KB)`);
  return 0;
}
